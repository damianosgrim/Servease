    private void loadCustomerRoomChargeDetails() {
    RoomCharge roomCharge = customer.getRoomCharge();
    if (roomCharge != null) {
        RoomChargeEnablebt.setSelected(roomCharge.isCardProvided());
        CreditLimit.setText(String.valueOf(roomCharge.getCreditLimit()));
    }
}

