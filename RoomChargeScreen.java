    private void SaveChangesbtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveChangesbtActionPerformed
        // TODO add your handling code here:
        boolean cardProvided = RoomChargeEnablebt.isSelected();
        double creditLimit = Double.parseDouble(CreditLimit.getText());
        String roomNumber = String.valueOf(customer.getRoomNumber());

        RoomCharge roomCharge = new RoomCharge(cardProvided, creditLimit, roomNumber);
        customer.activateRoomCharge(roomCharge);
        JOptionPane.showMessageDialog(this, "Room charge settings saved successfully!");
        
    }//GEN-LAST:event_SaveChangesbtActionPerformed

